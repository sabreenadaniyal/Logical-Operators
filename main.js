"use strict";
//LOGICAL OPERATORS
//AND(&&):
let a = 10;
let b = 5;
console.log((a < b) && (a > b));
//one more
let c = 10;
let d = 5;
let e = 15;
console.log((c < d) && (c > e));
//OR(||):
let f = 5;
let g = 15;
console.log((f > g) || (f < g));
//NOT(!)
let h = 20;
let i = 25;
console.log(!((h > i) && (h !== i)));
